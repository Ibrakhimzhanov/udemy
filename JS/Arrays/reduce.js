//#######################################################################
//              ##################################################
// @Reduce ##############################################
// ###############################################
//##############################

const arr = [232, 567, 31, 5, 7, 8, 12, 42, 23];

const reduceArray = (number) => {
  const reduceResult = number.reduce((acc, num) => {
    return acc + num;
  }, 0);
  return reduceResult;
};

console.log("Reduce: " + reduceArray(arr));

//#################################################################################################################################################
//              ###################################################################################################
// @Reduce  + Concat ##############################################
// #######################################################
//##############################

const arr = [232, [567, 31, 5, 7, 8], [12, 42], 23];

const reduceArray = (number) => {
  const reduceResult = number.reduce((acc, num) => {
    return acc.concat(num);
  }, []);
  return reduceResult;
};

console.log("Reduce + Contact: " + reduceArray(arr));
console.log(arr);
