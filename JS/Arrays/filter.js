const numbers = [9, 1, 78, 32, 6, 24, 3, 12, 64, 51];

const filterMethod = (number) => {
  const filterNumbers = number.filter((num) => {
    if (num % 3 === 0) {
      return true;
    }
    return false;
  });

  return filterNumbers;
};

console.log(filterMethod(numbers));

